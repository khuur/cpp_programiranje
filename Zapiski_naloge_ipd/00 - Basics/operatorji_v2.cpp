#include <iostream>
#include <cmath>

using namespace std;

int main() {


    int a;

    a = 5;
    cout << "a: " << a << endl;

    a = 10;
    cout << "a: " << a << endl;

    a = a + 13;
    cout << "a: " << a << endl;

    a += 13;
    cout << "a: " << a << endl;

    a = a - 5;
    cout << "a: " << a << endl;

    a -= 5;
    cout << "a: " << a << endl;

    a = a + 1;
    cout << "a: " << a << endl;

    a += 1;
    cout << "a: " << a << endl;

    a++;
    cout << "a: " << a << endl;

    a--;
    cout << "a: " << a << endl;


	return 0;
}
