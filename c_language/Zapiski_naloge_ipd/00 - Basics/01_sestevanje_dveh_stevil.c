// Ta program izračuna in izpiše vsoto dveh števil

#include <stdio.h>  // Knjižnica za funkcije za izpis in vnos

int main() {
    int num1 = 5;  // Prvo število
    int num2 = 10;  // Drugo število

    // Izračunamo in izpišemo vsoto števil
    printf("Vsota %d in %d je %d.\n", num1, num2, num1 + num2);

    return 0;  // Program uspešno končan
}
